public class PATTERN2 {
    public static void main(String[] args ){
PATTERN2(5);
    }
    static void PATTERN2(int n ){

        for(int i = 0; i < n; i++){
            for(int j = 0; j <n; j++){
                System.out.print(i+1);
            }
            System.out.println(" ");
        }
    }
}
