import java.util.*;
import java.lang.*;
import java.io.*;

/* Name of the class has to be "Main" only if the class is public. */
class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
	    
	    try {
	        
	        Scanner scan = new Scanner(System.in);
	        
	        int T = scan.nextInt();
	        while(T>0){
	            
	            int C = scan.nextInt();
	            int X = scan.nextInt();
	            int Y = scan.nextInt();
	            
	            int money = (C-X)*Y;
	            
	            
	            System.out.println(money);
	        }
	        
	        
	        
	        
	        
	    } catch(Exception e) {
	    }
	}
}
